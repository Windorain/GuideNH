package com.hfstudio.guidenh.guide.scene.ponder;

import org.jetbrains.annotations.Nullable;

public class PonderKeyframeEntityAnimation {

    @Nullable
    private String ref;
    @Nullable
    private String preset;
    @Nullable
    private Integer ticks;
    @Nullable
    private Double x;
    @Nullable
    private Double y;
    @Nullable
    private Double z;
    @Nullable
    private Double height;

    @Nullable
    public String getRef() {
        return ref;
    }

    @Nullable
    public String getPreset() {
        return preset;
    }

    @Nullable
    public Integer getTicks() {
        return ticks;
    }

    @Nullable
    public Double getX() {
        return x;
    }

    @Nullable
    public Double getY() {
        return y;
    }

    @Nullable
    public Double getZ() {
        return z;
    }

    @Nullable
    public Double getHeight() {
        return height;
    }
}
